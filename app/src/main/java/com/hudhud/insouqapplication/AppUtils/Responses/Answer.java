package com.hudhud.insouqapplication.AppUtils.Responses;

public class Answer {
    private String arAnswer, enAnswer;

    public Answer() {
        this.arAnswer = "";
        this.enAnswer = "";
    }

    public String getArAnswer() {
        return arAnswer;
    }

    public void setArAnswer(String arAnswer) {
        this.arAnswer = arAnswer;
    }

    public String getEnAnswer() {
        return enAnswer;
    }

    public void setEnAnswer(String enAnswer) {
        this.enAnswer = enAnswer;
    }
}
