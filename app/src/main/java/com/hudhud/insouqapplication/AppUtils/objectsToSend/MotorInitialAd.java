package com.hudhud.insouqapplication.AppUtils.objectsToSend;

public class MotorInitialAd {

    private String title, categoryId, brand, otherBrand, model, otherModel, trim, otherTrim, year;

    public MotorInitialAd() {
        this.title = "";
        this.categoryId = "";
        this.brand = "";
        this.otherBrand = "";
        this.model = "";
        this.otherModel = "";
        this.trim = "";
        this.otherTrim = "";
        this.year = "";
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(String categoryId) {
        this.categoryId = categoryId;
    }

    public String getBrand() {
        return brand;
    }

    public void setBrand(String brand) {
        this.brand = brand;
    }

    public String getOtherBrand() {
        return otherBrand;
    }

    public void setOtherBrand(String otherBrand) {
        this.otherBrand = otherBrand;
    }

    public String getModel() {
        return model;
    }

    public void setModel(String model) {
        this.model = model;
    }

    public String getOtherModel() {
        return otherModel;
    }

    public void setOtherModel(String otherModel) {
        this.otherModel = otherModel;
    }

    public String getTrim() {
        return trim;
    }

    public void setTrim(String trim) {
        this.trim = trim;
    }

    public String getOtherTrim() {
        return otherTrim;
    }

    public void setOtherTrim(String otherTrim) {
        this.otherTrim = otherTrim;
    }

    public String getYear() {
        return year;
    }

    public void setYear(String year) {
        this.year = year;
    }
}
