package com.hudhud.insouqapplication.AppUtils.objectsToSend;

public class AddMobileNumberAd {
    private String title, description, location, latitude, longitude, categoryId, operator, code, mobileNumberPlan, number, price, phoneNumber;

    public AddMobileNumberAd() {
        this.title = "";
        this.description = "";
        this.location = "";
        this.latitude = "";
        this.longitude = "";
        this.categoryId = "";
        this.operator = "";
        this.code = "";
        this.mobileNumberPlan = "";
        this.number = "";
        this.price = "";
        this.phoneNumber = "";
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }

    public String getLatitude() {
        return latitude;
    }

    public void setLatitude(String latitude) {
        this.latitude = latitude;
    }

    public String getLongitude() {
        return longitude;
    }

    public void setLongitude(String longitude) {
        this.longitude = longitude;
    }

    public String getCategoryId() {
        return categoryId;
    }

    public void setCategoryId(String categoryId) {
        this.categoryId = categoryId;
    }

    public String getOperator() {
        return operator;
    }

    public void setOperator(String operator) {
        this.operator = operator;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getMobileNumberPlan() {
        return mobileNumberPlan;
    }

    public void setMobileNumberPlan(String mobileNumberPlan) {
        this.mobileNumberPlan = mobileNumberPlan;
    }

    public String getNumber() {
        return number;
    }

    public void setNumber(String number) {
        this.number = number;
    }

    public String getPrice() {
        return price;
    }

    public void setPrice(String price) {
        this.price = price;
    }

    public String getPhoneNumber() {
        return phoneNumber;
    }

    public void setPhoneNumber(String phoneNumber) {
        this.phoneNumber = phoneNumber;
    }
}
